// Arrays:
//  Arrays are basically ordered list of values
//  They are non-primitive(or reference) data types
//  (means that they are stored and accessed by reference not value)

//  They are known as a special kind of objects because they
//  come in with numbered keys and
//  also built-in methods like push(), pop(), shift(), unshift(), map(), reduce(), filter()
//  that normal objects don't have

let arr = [1, 2, 3, 4]

// accessing elements of an array

console.log(arr[0])  //1
console.log(arr[1]) // 2

// NOTE: The indexing starts from 0


// Also, we can get the length of the array by doing
console.log(arr.length)  //4

// This returns the numbers of elements in the array

